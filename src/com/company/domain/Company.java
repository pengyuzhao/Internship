package com.company.domain;

public class Company {
 private int id;
 private String companyName;
 private String companyAccount;
 private String companyPwd;
 private String companyAddress;
 private String comStuId;	
 private int InternshipNums;
 
public int getId() {
	return id;
}
public void setId(int id) {
	this.id = id;
}
public String getCompanyName() {
	return companyName;
}
public void setCompanyName(String companyName) {
	this.companyName = companyName;
}
public String getCompanyAccount() {
	return companyAccount;
}
public void setCompanyAccount(String companyAccount) {
	this.companyAccount = companyAccount;
}
public String getCompanyPwd() {
	return companyPwd;
}
public void setCompanyPwd(String companyPwd) {
	this.companyPwd = companyPwd;
}
public String getCompanyAddress() {
	return companyAddress;
}
public void setCompanyAddress(String companyAddress) {
	this.companyAddress = companyAddress;
}
public String getComStuId() {
	return comStuId;
}
public void setComStuId(String comStuId) {
	this.comStuId = comStuId;
}
public int getInternshipNums() {
	return InternshipNums;
}
public void setInternshipNums(int internshipNums) {
	InternshipNums = internshipNums;
}
 
}
